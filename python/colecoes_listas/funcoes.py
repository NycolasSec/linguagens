
lista1 = [1, 3, 5, 7, 9]
lista2 = [2, 4, 6, 8, 0]


print("\nlista1 + lista2 --- ---")
lista_total = lista1 + lista2
print(lista_total)

# len retorna quantos elementos a lista tem
print("\nlen() --- ---")
print(len(lista_total))

## Funcoes para listas numericas

# retorna asoma de todos os elementos da lista.
print("\nsum() --- ---")
print(sum(lista1))

# Retorna o elemento de maior valor da lista.
print("\nmax() --- ---")
print(max(lista1))

# Retorna o elemento de menor valor da lista.
print("\nmin() --- ---")
print(min(lista2))

# == Igualdade
if (2 == (4 / 2)):
    print("São iguais.")

if (2 == 2.0):
    print("São iguais.")

# != Diferenca
if (2 != 4):
    print("São diferentes.")

# > Maior que
if ((2 + 5) > 4):
    print("O primeiro é maior que o segundo.")

# < Menor que
if (2 < 4):
    print("O primeiro é menor que o segundo.")


# >= Maior ou Igual a
if (9 >= 4):
    print("O primeiro é maior ou igual o segundo.")

# <= Menor ou Igual a
if (4 <= 4):
    print("O primeiro é menor ou igual o segundo.")
